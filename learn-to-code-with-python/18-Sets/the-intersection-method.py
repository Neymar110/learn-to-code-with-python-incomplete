# The intersection method takes a set as an argument and returns the elements in the set that are alike to the original list

# It can also be called with an Ampersand (&)

set1 = {"Messi", "Ronaldinio", "Ronaldo", "Luis Figo", "Zlatan", "Zidane", "Neymar", "Mbappe"}

set2 = {"Ramos", "Pepe", "Ronaldinio", "Maradona", "Luis Figo"}

print(set1.intersection(set2))

print(set1 & set2)
