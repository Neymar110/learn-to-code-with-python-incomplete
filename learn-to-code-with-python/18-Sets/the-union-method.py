# The union method takes a set as an argument and merges or unites them together while removing all duplicates in the process

# It also takes the vertical bar or | symbol

set1 = {"Messi", "Ronaldinio", "Ronaldo", "Luis Figo", "Zlatan", "Zidane", "Neymar", "Mbappe"}

set2 = {"Ramos", "Pepe", "Ronaldinio", "Maradona", "Luis Figo"}

print(set1.union(set2))
print(set1 | set2)